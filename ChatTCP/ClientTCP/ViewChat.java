    private class Ascoltatore extends Thread {

        @Override
        public void run() {
            boolean continua = true;
            while (continua) {
                try {
                    String op = in.readLine();
                    if (op.equals("Leggi")) {
                        if (idChat == Integer.parseInt(in.readLine())) {
                            conversazione.setText(conversazione.getText() + in.readLine() + "\n\n");
                        }
                    } else if (op.equals("Rimuovi")) {
                        contattiComboBox.removeItem(in.readLine());
                    } else if (op.equals("")) {
                        continua = false;
                        dispose();
                        ViewUsers v = new ViewUsers(in, out);
                        v.setVisible(true);
                    }
                } catch (IOException ex) {
                    Logger.getLogger(ViewChat.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }

    private BufferedReader in;
    private PrintWriter out;
    private int idChat;

    /**
     * Creates new form VistaChat
     */
    public ViewChat(BufferedReader in, PrintWriter out, int idChat) throws IOException {
        this.in = in;
        this.out = out;
        this.idChat = idChat;
        initComponents();
        out.println("Carica");
        if (in.readLine().equals("Gruppo")) {
            int nContatti = Integer.parseInt(in.readLine());
            for (int i = 0; i < nContatti; i++) {
                contattiComboBox.addItem(in.readLine());
            }
        } else {
            contattiComboBox.setVisible(false);
            btnAggiungi.setVisible(false);
        }
        int nMessaggi = Integer.parseInt(in.readLine());
        for (int i = 0; i < nMessaggi; i++) {
            String testo = in.readLine();
            conversazione.setText(conversazione.getText() + testo + "\n\n");
        }
        new Ascoltatore().start();
    }

